package aColorier;

public class PhonemeGraphemeCouleur {
	
	private String phoneme;
	private String grapheme;
	private String nomCouleurBas;
	private String nomCouleurHaut;	

	public String getPhoneme() {
		return phoneme;
	}
	public void setPhoneme(String phoneme) {
		this.phoneme = phoneme;
	}
	public String getGraphemes() {
		return grapheme;
	}
	public void setGraphemes(String graphemes) {
		this.grapheme = graphemes;
	}
	public String getNomCouleurBas() {
		return nomCouleurBas;
	}
	public void setNomCouleurBas(String nomCouleurBas) {
		this.nomCouleurBas = nomCouleurBas;
	}
	public String getNomCouleurHaut() {
		return nomCouleurHaut;
	}
	public void setNomCouleurHaut(String nomCouleurHaut) {
		this.nomCouleurHaut = nomCouleurHaut;
	}

}
